package jcm2606.mods.jccore.util;

public class Coord {
    public double x;
    public double y;
    public double z;
    
    public Coord(int x, int y, int z)
    {
        this.x = x;
        this.y = y;
        this.z = z;
    }
    
    public Coord(float x, float y, float z)
    {
        this.x = x;
        this.y = y;
        this.z = z;
    }
    
    public Coord(double x, double y, double z)
    {
        this.x = x;
        this.y = y;
        this.z = z;
    }
}
